package hr.fer.zemris.evolutionary.task4;

public class FunctionValues {

	private double x;
	private double y;
	private double value;
	
	public FunctionValues(double x, double y, double value) {
		this.x = x;
		this.y = y;
		this.value = value;
	}

	public double getX() {
		return x;
	}

	public double getY() {
		return y;
	}

	public double getValue() {
		return value;
	}
	
}
